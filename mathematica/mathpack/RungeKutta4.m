RungeKutta4[f_, t_, y_, h_]:= Module[
	{k1, k2, k3, k4},
	k1 = f[t, y];
	k2 = f[t+h/2, y+k1*h/2];
	k3 = f[t+h/2, y+k2*h/2];
	k4 = f[t+h, y+k3*h];
	y+(k1+2*k2+2*k3+k4)*h/6
	]